    public Boolean validaCampos() {
        String mensagem = "";
        if (pNome.getText().equals("")) {
            mensagem = "O nome é obrigatório!\n";
        }
        if (pPreco.getText().equals("")) {
            mensagem = mensagem + "O preço é obrigatório!\n";
        }
        if (pEstoque.getText().equals("")) {
            mensagem = mensagem + "A quantidade é obrigatória!";
        }
        if (pGrupodeProduto.getSelectedItem().equals("")) {
            mensagem = mensagem + "O Grupo de produto é obrigatório!";
        }
        if (pMarca.getSelectedItem().equals("")) {
            mensagem = mensagem + "A marca é obrigatória!";
        }
        if (!mensagem.equals("")) {
            JOptionPane.showMessageDialog(null, mensagem);
            return false;
        } else {
            return true;
        }
    }
    public void limparCampos() {
        cId.setText("");
        pNome.setText("");
        pPreco.setText("");
        pEstoque.setText("");
        pGrupodeProduto.setSelectedItem(null);
        pMarca.setSelectedItem(null);
    }
    
    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        if (validaCampos()) {

            pro.setNome(pNome.getText());
            pro.setPreco(Double.parseDouble(pPreco.getText().replace(".", "").replace(",", ".")));
            pro.setEstoque(Double.parseDouble(pEstoque.getText().replace(".", "")));
            pro.setGrupoproduto(listaGrupoProduto.get(pGrupodeProduto.getSelectedIndex()));
            pro.setMarca(listaMarca.get(pMarca.getSelectedIndex()));

            HibernateUtil.beginTransaction();
            HibernateUtil.getSession().persist(pro);
            HibernateUtil.commitTransaction();
            HibernateUtil.closeSession();

            montaTabela();

            pNome.setText("");
            pPreco.setText("");
            pEstoque.setText("");
            pGrupodeProduto.setSelectedItem(null);
            pMarca.setSelectedItem(null);
            validaTela("inicio");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btSalvarActionPerformed

