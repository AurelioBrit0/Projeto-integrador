    public void validaTela(String operacao) {
        if (operacao.equals("inicio")) {
            pNome.setEnabled(false);
            pPreco.setEnabled(false);
            pMarca.setEnabled(false);
            pGrupodeProduto.setEnabled(false);
            pEstoque.setEnabled(false);
            btNovo.setEnabled(true);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
            btSalvar.setEnabled(false);
            btCancelar.setEnabled(false);
            btSair.setEnabled(true);
        } else if (operacao.equals("novo")) {
            pNome.setEnabled(true);
            pPreco.setEnabled(true);
            pEstoque.setEnabled(true);
            pMarca.setEnabled(true);
            pGrupodeProduto.setEnabled(true);
            btNovo.setEnabled(false);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
            btSalvar.setEnabled(true);
            btCancelar.setEnabled(true);
            btSair.setEnabled(false);
        } else if (operacao.equals("selecionado")) {
            pNome.setEnabled(false);
            pPreco.setEnabled(false);
            pEstoque.setEnabled(false);
            pMarca.setEnabled(false);
            pGrupodeProduto.setEnabled(false);
            btNovo.setEnabled(true);
            btEditar.setEnabled(true);
            btExcluir.setEnabled(true);
            btSalvar.setEnabled(false);
            btCancelar.setEnabled(false);
            btSair.setEnabled(true);
        }
    }
    
    public void montaCombo () {
    pGrupodeProduto.removeAllItems();
    listaGrupoProduto = HibernateUtil.getSession().createQuery("From GrupoProduto").list();
    for(GrupoProduto c : listaGrupoProduto){
    pGrupodeProduto.addItem(c.getNome());
    }
    
     pMarca.removeAllItems();
    listaMarca = HibernateUtil.getSession().createQuery("From Marca").list();
    for(Marca c : listaMarca){
    pMarca.addItem(c.getNome());
    }
    
    }
    
