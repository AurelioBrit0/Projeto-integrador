    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        if (validaCampos()) {

            
            
            os.setDescricao(dDescricao.getText());
            os.setdataOrdemdeServico(dDate.getDate());
            os.setStatus(dStatus.getText());
            os.setCliente(listaCliente.get(dCliente.getSelectedIndex()));
            
            

            HibernateUtil.beginTransaction();
            HibernateUtil.getSession().persist(os);
            HibernateUtil.commitTransaction();
            HibernateUtil.closeSession();

            montaTabela();

            dDescricao.setText("");
            dDate.setCalendar(null);
            dStatus.setText("");
            dCliente.setSelectedItem(null);
            validaTela("inicio");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btSalvarActionPerformed

