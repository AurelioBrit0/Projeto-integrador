    public void validaTela(String operacao) {
        if (operacao.equals("inicio")) {
            dDescricao.setEnabled(false);
            dDate.setEnabled(false);
            dCliente.setEnabled(false);
            dStatus.setEnabled(false);
            btNovo.setEnabled(true);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
            btSalvar.setEnabled(false);
            btCancelar.setEnabled(false);
            btSair.setEnabled(true); 
        } else if (operacao.equals("novo")) {
            dDescricao.setEnabled(true);
            dDate.setEnabled(true);
            dStatus.setEnabled(true);
            dCliente.setEnabled(true);
            btNovo.setEnabled(false);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
            btSalvar.setEnabled(true);
            btCancelar.setEnabled(true);
            btSair.setEnabled(false);
        } else if (operacao.equals("selecionado")) {
            dDescricao.setEnabled(false);
            dDate.setEnabled(false);
            dStatus.setEnabled(false);
            dCliente.setEnabled(false);
            btNovo.setEnabled(true);
            btEditar.setEnabled(true);
            btExcluir.setEnabled(true);
            btSalvar.setEnabled(false);
            btCancelar.setEnabled(false);
            btSair.setEnabled(true);
        }
    }
    
