    public Boolean validaCampos() {
        String mensagem = "";
        if (cNome.getText().equals("")) {
            mensagem = "O nome é obrigatório!\n";
        }
        if (cCNPJ.getText().equals("")) {
            mensagem = mensagem + "O CNPJ é obrigatória!\n";
        }
        if (cTelefone.getText().equals("")) {
            mensagem = mensagem + "O telefone é obrigatória!";
        }
        if (cEndereco.getText().equals("")) {
            mensagem = mensagem + "O endereço é obrigatória!";
        }
        if (!mensagem.equals("")) {
            JOptionPane.showMessageDialog(null, mensagem);
            return false;
        } else {
            return true;
        }
    }
    public void limparCampos() {
        cId.setText("");
        cNome.setText("");
        cCNPJ.setText("");
        cTelefone.setText("");
        cEndereco.setText("");
        cCidade.setSelectedItem(null);
    }
    
